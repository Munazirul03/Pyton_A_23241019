#ARITMATIKA
y=9
x=3

#penjumlahan +
hasil = x + y
print(x, '+' , y, '=', hasil)

#pengurangan -
hasil = x - y
print(x, '-' , y, '=', hasil)

#perkalian *
hasil = x * y
print(x, 'x' , y, '=', hasil)

#pembagian /
hasil = x / y
print(x, ':' , y, '=', hasil)

#perpangkatan **
hasil = x ** y
print(x, '^' , y, '=', hasil)

#modulus %
hasil = x % y
print(x, 'mod' , y, '=', hasil)

#floor devision //
hasil = x // y
print(x, 'floor' , y, '=', hasil)

