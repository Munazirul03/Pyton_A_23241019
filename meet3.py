# belajat tipe data

# Tipe data integer : bilangan bulat
bil_integer = 100
print (type(bil_integer))
print ("type data dari bil_integer adalah :", (type(bil_integer)))

#tipe bilangan float : bilangan desimal
bil_float = 2.5 
print (type(bil_float))
print ("type data dari bil_float adalah :", (type(bil_float)))

#tipe data boolean : 0/1, true/false, ya/tidak
bil_bol = False 
print (type(bil_bol))
print ("type data dari bil_bol adalah :", (type(bil_bol)))

# tipe data string : kumpulan karakter
bil_string = "300000.5"
print (type(bil_string))
print ("type data dari bil_string adalah :", (type(bil_string)))
