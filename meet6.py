# Perbandingan >, <, =, is, isnot, ==, !=

x = 3
y = 4

# lebih besar >
hasil = x > y 
print (x, '>', y, '=', hasil)

# lebih kecil <
hasil = x < y 
print (x, '<', y, '=', hasil)

# lebih besar >=
hasil = x >= y 
print (x, '>=', y, '=', hasil)
print (x, '>=', 3, '=', hasil)

# lebih kecil <=
hasil = x <= y 
print (x, '<=', y, '=', hasil)
print (x, '<=', 3, '=', hasil)

# is (adalah)
hasil = x is 9
print(x, 'is', 9 , '=', hasil)

# is-not (bukan)
hasil = x is not 9
print(x, 'is not', 9 , '=', hasil)

# ==
hasil = x == 3
print(x, '==', 3 , '=',  hasil)

# !=
hasil = x != 5
print(x,'!=',5, '=', hasil)


